/// Hardcoded list of that are known to be from system libraries and other
/// non-postgres sources, which we exclude from wrappers to avoid symbol
/// versioning pain. This is quite hacky, and is done as a stopgap to avoid to
/// forcing pgrx extensions to require a newer version of glibc than they might
/// otherwise. (As a result, this only covers exported functions, and not types,
/// constants, and other things we may not want in the `pgrx-pg-sys` wrappers)
///
/// The list was generated with something like the following `fish-shell`
/// script, run on a Fedora-family Linux system, and followed with editing (to
/// write this comment, for example):
///
/// ```fish
/// begin
///     printf "pub(super) const EXCLUDED_SYMS: &[&str] = &["
///     for lib in libc.so.6 libm.so.6 libresolv.so.2 libpthread.so.0 librt.so.1 libdl.so.2
///         nm -D --defined-only "/usr/lib64/$lib"
///     end | string replace -fr '^[\da-fA-F]+\s+[^AU]\s+([^\s@]+)?(@\S+)?$' '$1' \
///         | sort | uniq | xargs printf '\"%s\",' | string join ''
///     printf "]"
/// end | rustfmt
/// ```
///
/// We use it on non-linux platforms as well, since it does a decent job at
/// approximating the set of symbols we'd like to exclude, but it's not perfect
/// anywhere (but especially not on other targets).
///
